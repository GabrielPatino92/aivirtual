# Vamos a jugar con cadenas en Python. 
# Una cadena es como una cuerda de letras, palabras o frases.

# Ejemplo 1: Crear una cadena
mi_cadena = "Hola, soy una cadena"
print(mi_cadena)

# Ejemplo 2: Unir cadenas
cadena1 = "Hola"
cadena2 = "Mundo"
cadena_unida = cadena1 + " " + cadena2
print(cadena_unida)

# Ejemplo 3: Repetir cadenas
cadena_repetida = "¡Python! " * 3
print(cadena_repetida)

# Ejemplo 4: Saber cuántas letras tiene una cadena
longitud = len(mi_cadena)
print(f"La cadena tiene {longitud} caracteres.")

# Ejemplo 5: Cambiar a mayúsculas o minúsculas
cadena_mayus = mi_cadena.upper()
cadena_minus = mi_cadena.lower()
print(cadena_mayus)
print(cadena_minus)

# ¡Y así puedes jugar con cadenas en Python!